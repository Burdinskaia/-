num_1 = int(input('Ievadiet pirmo skaitli: ')) #iespēja ievadīt pirmo skaitli
num_2 = int(input('Ievadiet otro skaitli: ')) #iespēja ievadīt otro skaitli

print('{} + {} = '.format(num_1, num_2)) #darbība ar mainīgajiem - skaitīšana
print(num_1 + num_2) #izvadīt rezultātu uz ekrāna

print('{} - {} = '.format(num_1, num_2)) #darbība ar mainīgajiem - atņemšana
print(num_1 - num_2) #izvadīt rezultātu uz ekrāna

print('{} * {} = '.format(num_1, num_2)) #darbība ar mainīgajiem - reizināšana
print(num_1 * num_2) #izvadīt rezultātu uz ekrāna

print('{} / {} = '.format(num_1, num_2)) #darbība ar mainīgajiem - dalīšana
print(num_1 / num_2) #izvadīt rezultātu uz ekrāna